#include <unistd.h>

void	ft_putchar(char a, char b)
{
	write(1, &a, 1);
	write(1, &b, 1);
}

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < 98)
	{
		j = i + 1;
		while (j < 100)
		{
			ft_putchar(((i / 10) + '0'), ((i % 10) + '0'));
			write(1, " ", 1);
			ft_putchar(((j / 10) + '0'), ((j % 10) + '0'));
			write(1, ", ", 2);
			j++;
		}
		i++;
	}
	write(1, "98 99", 5);
}
