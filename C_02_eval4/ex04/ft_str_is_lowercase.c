#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	if (str[0] == '\0')
		return (1);
	while (*str != '\0')
	{
		if ((*str < 97) || (*str > 122))
			return (0);
		str++;
	}
	return (1);
}
